#include <unistd.h>

void	show(char **matrix) 
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			write (1, &matrix[4][4], 28);
			j++;
		}
		i++;
	}
}
